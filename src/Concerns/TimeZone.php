    /**
     * Return timezone by country code and subdivision code
     *
     * @param string $country_code
     * @param string|null $subdivision_code
     * @return string|null
     */
    public function getTimeZone(string $country_code, string $subdivision_code = null): string|null
    {
        if (isset($this->timezones[$country_code]) && !is_array($this->timezones[$country_code])) {
            return $this->timezones[$country_code];
        }
        if(empty($subdivision_code)) {
            if(isset($this->timezones[$country_code]) && is_array($this->timezones[$country_code])) {
                return $this->default_timezones[$country_code];
            }
        } elseif (isset($this->timezones[$country_code][$subdivision_code])) {
            return $this->timezones[$country_code][$subdivision_code];
        }
        return null;
    }

